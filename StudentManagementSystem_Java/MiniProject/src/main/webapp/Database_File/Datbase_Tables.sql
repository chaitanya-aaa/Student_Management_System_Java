CREATE TABLE Students (id INT PRIMARY KEY AUTO_INCREMENT, 
					roll_no VARCHAR(20), 
					name1 VARCHAR(50), 
                    username VARCHAR(50), 
                    email VARCHAR(50), 
                    age INT, 
                    branch VARCHAR(50), 
                    section VARCHAR(50), 
                    gender VARCHAR(50), 
                    mobile BIGINT ,
                    address VARCHAR(100));
				